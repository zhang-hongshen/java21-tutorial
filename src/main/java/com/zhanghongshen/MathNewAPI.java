package com.zhanghongshen;

public class MathNewAPI {

    public static void main(String[] args) {
        System.out.println(Math.ceilDiv(5, 3));
        System.out.println(Math.ceilDivExact(5, 3));
        System.out.println(Math.ceilMod(5, -3));
        System.out.println(Math.floorDiv(5, 3));
    }
}
